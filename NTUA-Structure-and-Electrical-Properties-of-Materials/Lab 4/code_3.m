x=linspace(-0.0001,0.0001,100)
tp=17.*(10.^(-6))
Dp=10.^(-3)
Pn=10.^12.*10.^6
Pno=(10.^5).*(10.^6)
Pn0=(10.^12).*(10.^6)
N=(10.^12).*(10.^6)
t1=10.^(-8)
t2=10.^(-6)

a1=sqrt(4.*pi.*Dp.*t1)
b1=N./(a1)
c1=-((x.^2)/(4.*Dp.*t1))-(t1./tp)
y1=Pno.+(b1.*e.^c1)

a2=sqrt(4.*pi.*Dp.*t2)
b2=N./(a2)
c2=-((x.^2)/(4.*Dp.*t2))-(t2./tp)
y2=Pno.+(b2.*e.^c2)

plot(x,y1,x,y2)